    resource: String,
    screenshotText: String,
    onClick: () -> Unit = { },
) {

    Column(
        modifier = ScreenshotCardStyle.toModifier()
            .cursor(Cursor.Pointer)
            .onClick { onClick() },
        horizontalAlignment = Alignment.CenterHorizontally,
        verticalArrangement = Arrangement.spacedBy(8.px)
    ) {
        Image(
            src = resource,
            alt = "Project Screenshot",
        )

        Text(
            screenshotText,
        )
    }

}